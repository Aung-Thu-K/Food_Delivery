package com.atkandroid.foodgreen;

public class model {
    private String name,rest_name,rate,price;
    private int img;

    public model() {
    }

    public model(String name, String rest_name, String rate, String price, int img) {
        this.name = name;
        this.rest_name = rest_name;
        this.rate = rate;
        this.price = price;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRest_name() {
        return rest_name;
    }

    public void setRest_name(String rest_name) {
        this.rest_name = rest_name;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
